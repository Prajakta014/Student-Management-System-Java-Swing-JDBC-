    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String studentRollNumber = sRollnumber.getText();
DefaultTableModel model = (DefaultTableModel) table.getModel();

if (studentRollNumber.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Please Enter Roll Number");
    return;
}

try {
    Class.forName("com.mysql.cj.jdbc.Driver");

    Connection con = DriverManager.getConnection(
        "jdbc:mysql://127.0.0.1:3308/collage2",
        "root",
        "Root@2025"
    );

    String query = "SELECT * FROM collage WHERE RollNumber = ?";
    PreparedStatement ps = con.prepareStatement(query);
    ps.setString(1, studentRollNumber);

    ResultSet rs = ps.executeQuery();

    model.setRowCount(0); // clear table

    boolean hasResult = false;

    while (rs.next()) {
        hasResult = true;

        String roll = rs.getString("RollNumber");
        String name = rs.getString("Name");
        String studentClass = rs.getString("StudentClass");

        String row[] = { roll, name, studentClass };
        model.addRow(row);
    }

    if (!hasResult) {
        JOptionPane.showMessageDialog(this, "No Result Found");
    }

    rs.close();
    ps.close();
    con.close();

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, e.getMessage());
}
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

